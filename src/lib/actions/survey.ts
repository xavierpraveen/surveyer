// ─── Question Actions ─────────────────────────────────────────────────────────

export async function createQuestion(
  input: unknown
): Promise<{ success: true; data: SurveyQuestion } | { success: false; error: string }> {
  const parsed = createQuestionSchema.safeParse(input)
  if (!parsed.success) {
    return { success: false, error: parsed.error.errors[0].message }
  }

  const { options, ...questionFields } = parsed.data

  const { count } = await db
    .from('questions')
    .select('*', { count: 'exact', head: true })
    .eq('survey_section_id', questionFields.survey_section_id)

  const stableQuestionId = crypto.randomUUID()
  const originalPayload: Record<string, unknown> = {
    ...questionFields,
    display_order: (count ?? 0),
    stable_question_id: stableQuestionId,
  }
  let payload: Record<string, unknown> = { ...originalPayload }
  let questionData: unknown = null
  let lastError: { message?: string } | null = null

  for (let attempt = 0; attempt < 6; attempt++) {
    const insertResult = await db
      .from('questions')
      .insert(payload)
      .select()
      .single()

    if (!insertResult.error) {
      questionData = insertResult.data
      break
    }

    lastError = insertResult.error as { message?: string }
    if (!shouldRetryWithLegacyQuestionWriteColumns(insertResult.error)) {
      return { success: false, error: insertResult.error.message }
    }

    const missingCol = getMissingColumnName(insertResult.error)
    if (missingCol === 'question_type') {
      delete payload.question_type
      payload.type = parsed.data.question_type
      continue
    }
    if (missingCol === 'is_required') {
      delete payload.is_required
      payload.required = parsed.data.is_required
      continue
    }
    if (missingCol === 'conditional_rule') {
      delete payload.conditional_rule
      payload.conditional_config =
        parsed.data.conditional_rule && typeof parsed.data.conditional_rule === 'object'
          ? parsed.data.conditional_rule
          : {}
      continue
    }
    if (missingCol === 'stable_question_id') {
      delete payload.stable_question_id
      continue
    }
    if (missingCol) {
      delete payload[missingCol]
      continue
    }

    payload = buildLegacyQuestionPayload(payload)
  }

  if (!questionData) {
    return { success: false, error: lastError?.message ?? 'Failed to create question' }
  }

  if (options && options.length > 0) {
    const optionResult = await insertQuestionOptions((questionData as { id: string }).id, options)
    if (!optionResult.success) return optionResult
  }

  return { success: true, data: normalizeQuestionRow(questionData as Record<string, unknown>) }
}

export async function updateQuestion(
  input: unknown
): Promise<{ success: true; data: SurveyQuestion } | { success: false; error: string }> {
  const parsed = updateQuestionSchema.safeParse(input)
  if (!parsed.success) {
    return { success: false, error: parsed.error.errors[0].message }
  }

  const { id, options, ...fields } = parsed.data
  const modernUpdate = await db
    .from('questions')
    .update(fields)
    .eq('id', id)
    .select()
    .single()

  let data: unknown = modernUpdate.data
  if (modernUpdate.error) {
    if (!shouldRetryWithLegacyQuestionWriteColumns(modernUpdate.error)) {
      return { success: false, error: modernUpdate.error.message }
    }

    const legacyFields = buildLegacyQuestionPayload(fields as Record<string, unknown>)
    const legacyUpdate = await db
      .from('questions')
      .update(legacyFields)
      .eq('id', id)
      .select()
      .single()

    if (legacyUpdate.error) return { success: false, error: legacyUpdate.error.message }
    data = legacyUpdate.data
  }

  if (options !== undefined && id) {
    await db.from('question_options').delete().eq('question_id', id)
    if (options.length > 0) {
      const optionResult = await insertQuestionOptions(id, options)
      if (!optionResult.success) return optionResult
    }
  }

  return { success: true, data: normalizeQuestionRow(data as Record<string, unknown>) }
}

