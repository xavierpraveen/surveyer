async function ensureDefaultSurveySection(surveyId: string): Promise<void> {
  const { count, error: countError } = await db
    .from('survey_sections')
    .select('*', { count: 'exact', head: true })
    .eq('survey_id', surveyId)

  if (countError || (count ?? 0) > 0) return

  await db
    .from('survey_sections')
    .insert({
      survey_id: surveyId,
      title: 'About You',
      description: 'Add respondent profile and context questions here.',
      target_roles: ['all'],
      display_order: 0,
    })
}

async function insertQuestionOptions(
  questionId: string,
  options: Array<{ text: string; display_order: number }>
): Promise<{ success: true } | { success: false; error: string }> {
  if (options.length === 0) return { success: true }

  const modernOptions = options.map((opt) => ({
    question_id: questionId,
    text: opt.text,
    value: opt.text,
    display_order: opt.display_order,
  }))

  const modernInsert = await db.from('question_options').insert(modernOptions)
  if (!modernInsert.error) return { success: true }

  const missingCol = getMissingColumnName(modernInsert.error)
  if (missingCol !== 'value') {
    return { success: false, error: modernInsert.error.message }
  }

  const legacyOptions = options.map((opt) => ({
    question_id: questionId,
    text: opt.text,
    display_order: opt.display_order,
  }))
  const legacyInsert = await db.from('question_options').insert(legacyOptions)
  if (legacyInsert.error) return { success: false, error: legacyInsert.error.message }
  return { success: true }
}

