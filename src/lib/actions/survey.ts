export async function mapQuestionDimensions(
  input: unknown
): Promise<{ success: true } | { success: false; error: string }> {
  const parsed = mapDimensionsSchema.safeParse(input)
  if (!parsed.success) {
    return { success: false, error: parsed.error.errors[0].message }
  }

  const { question_id, dimension_ids } = parsed.data

  const { error: deleteError } = await db
    .from('question_dimension_map')
    .delete()
    .eq('question_id', question_id)

  if (deleteError) return { success: false, error: deleteError.message }

  if (dimension_ids.length > 0) {
    const { error: insertError } = await db
      .from('question_dimension_map')
      .insert(dimension_ids.map((dimension_id: string) => ({ question_id, dimension_id })))
    if (insertError) return { success: false, error: insertError.message }
  }

  return { success: true }
}

// ─── Lifecycle Transition ─────────────────────────────────────────────────────

export async function transitionSurveyStatus(
  input: unknown
): Promise<{ success: true; data: Survey } | { success: false; error: string }> {
  const parsed = transitionStatusSchema.safeParse(input)
  if (!parsed.success) {
    return { success: false, error: parsed.error.errors[0].message }
  }

  const { survey_id, to_status, opens_at } = parsed.data

  const { data: current, error: fetchError } = await db
    .from('surveys')
    .select('status')
    .eq('id', survey_id)
    .single()

  if (fetchError || !current) {
    return { success: false, error: 'Survey not found' }
  }

  const currentStatus = (current as { status: SurveyStatus }).status
  const allowed = ALLOWED_TRANSITIONS[currentStatus]

  if (!allowed.includes(to_status)) {
    return { success: false, error: 'Invalid status transition' }
  }

  const updateFields: Record<string, unknown> = { status: to_status }
  if (opens_at) updateFields.opens_at = opens_at

  const { data, error } = await db
    .from('surveys')
    .update(updateFields)
    .eq('id', survey_id)
    .select()
    .single()

  if (error) return { success: false, error: error.message }
  return { success: true, data: data as Survey }
}

// ─── Duplicate Survey ─────────────────────────────────────────────────────────

export async function duplicateSurvey(
  surveyId: string
): Promise<{ success: true; data: Survey } | { success: false; error: string }> {
  // Fetch source survey
  const { data: sourceSurvey, error: surveyFetchError } = await db
    .from('surveys')
    .select('*')
    .eq('id', surveyId)
    .single()

  if (surveyFetchError || !sourceSurvey) {
    return { success: false, error: 'Survey not found' }
  }

  // Fetch all sections
  const { data: sections, error: sectionsFetchError } = await db
    .from('survey_sections')
    .select('*')
    .eq('survey_id', surveyId)
    .order('display_order')

  if (sectionsFetchError) {
    return { success: false, error: sectionsFetchError.message }
  }

  const sectionList = (sections ?? []) as Array<SurveySection & { created_at?: string }>
  const sectionIds = sectionList.map((s) => s.id)

  let questions: Array<SurveyQuestion & { created_at?: string }> = []
  let questionIds: string[] = []

  if (sectionIds.length > 0) {
    const { data: qs, error: questionsFetchError } = await db
      .from('questions')
      .select('*')
      .in('survey_section_id', sectionIds)
      .order('display_order')

    if (questionsFetchError) {
      return { success: false, error: questionsFetchError.message }
    }

    questions = (qs ?? []) as Array<SurveyQuestion & { created_at?: string }>
    questionIds = questions.map((q) => q.id)
  }

  let allOptions: Array<{ id: string; question_id: string; text: string; display_order: number }> = []
  if (questionIds.length > 0) {
    const { data: opts, error: optsFetchError } = await db
      .from('question_options')
      .select('*')
      .in('question_id', questionIds)
      .order('display_order')

    if (optsFetchError) {
      return { success: false, error: optsFetchError.message }
    }
    allOptions = (opts ?? []) as typeof allOptions
  }

  let allDimensionMaps: Array<{ question_id: string; dimension_id: string }> = []
  if (questionIds.length > 0) {
    const { data: dimMaps, error: dimMapsFetchError } = await db
      .from('question_dimension_map')
      .select('*')
      .in('question_id', questionIds)

    if (dimMapsFetchError) {
      return { success: false, error: dimMapsFetchError.message }
    }
    allDimensionMaps = (dimMaps ?? []) as typeof allDimensionMaps
  }

  // Create new survey (status='draft', new id)
  const { id: _id, created_at: _ca, status: _st, ...surveyFields } = sourceSurvey as Survey & { created_at: string }

  const { data: newSurvey, error: newSurveyError } = await db
    .from('surveys')
    .insert({
      ...surveyFields,
      status: 'draft',
      title: `Copy of ${(sourceSurvey as Survey).title}`,
    })
    .select()
    .single()

  if (newSurveyError || !newSurvey) {
    return { success: false, error: newSurveyError?.message ?? 'Failed to create survey copy' }
  }

  const newSurveyId = (newSurvey as { id: string }).id

  // Duplicate sections — build old→new id map
  const sectionIdMap: Record<string, string> = {}
  for (const section of sectionList) {
    const { id: sectionId, survey_id: _sv, created_at: _sca, ...sectionFields } = section
    const { data: newSection, error: newSectionError } = await db
      .from('survey_sections')
      .insert({ ...sectionFields, survey_id: newSurveyId })
      .select()
      .single()

    if (newSectionError || !newSection) {
      return { success: false, error: newSectionError?.message ?? 'Failed to copy section' }
    }
    sectionIdMap[sectionId] = (newSection as { id: string }).id
  }

  // Duplicate questions — preserve stable_question_id
  const questionIdMap: Record<string, string> = {}
  for (const question of questions) {
    const { id: questionId, survey_section_id, created_at: _qca, ...questionFields } = question
    const newSectionId = sectionIdMap[survey_section_id]
    if (!newSectionId) continue

    const { data: newQuestion, error: newQuestionError } = await db
      .from('questions')
      .insert({ ...questionFields, survey_section_id: newSectionId })
      .select()
      .single()

    if (newQuestionError || !newQuestion) {
      return { success: false, error: newQuestionError?.message ?? 'Failed to copy question' }
    }
    questionIdMap[questionId] = (newQuestion as { id: string }).id
  }

  // Duplicate question_options
  for (const option of allOptions) {
    const { id: _optId, question_id, ...optFields } = option
    const newQuestionId = questionIdMap[question_id]
    if (!newQuestionId) continue

    const { error: optError } = await db
      .from('question_options')
      .insert({ ...optFields, question_id: newQuestionId })

    if (optError) return { success: false, error: optError.message }
  }

  // Duplicate question_dimension_map
  for (const dimMap of allDimensionMaps) {
    const { question_id, dimension_id } = dimMap
    const newQuestionId = questionIdMap[question_id]
    if (!newQuestionId) continue

    const { error: dimError } = await db
      .from('question_dimension_map')
      .insert({ question_id: newQuestionId, dimension_id })

    if (dimError) return { success: false, error: dimError.message }
  }

  return { success: true, data: newSurvey as Survey }
}

// ─── Delete Survey ────────────────────────────────────────────────────────────

export async function deleteSurvey(
  surveyId: string
): Promise<{ success: true } | { success: false; error: string }> {
  const supabase = await createSupabaseServerClient()
  const { data: { user }, error: authError } = await supabase.auth.getUser()
  if (authError || !user) return { success: false, error: 'Unauthorized' }

  const role = normalizeRole(user.app_metadata?.role as string | undefined)
  if (role !== 'admin') return { success: false, error: 'Forbidden' }

  const { data: surveyData, error: surveyError } = await db
    .from('surveys')
    .select('id')
    .eq('id', surveyId)
    .single()

  if (surveyError || !surveyData) return { success: false, error: 'Survey not found' }

  const { count: snapshotCount, error: snapshotError } = await db
    .from('publication_snapshots')
    .select('*', { count: 'exact', head: true })
    .eq('survey_id', surveyId)

  if (snapshotError) return { success: false, error: snapshotError.message }
  if ((snapshotCount ?? 0) > 0) {
    return { success: false, error: 'Cannot delete a survey with published results.' }
  }

  const { error: actionItemsError } = await db
    .from('action_items')
    .update({ survey_id: null })
    .eq('survey_id', surveyId)
  if (actionItemsError) return { success: false, error: actionItemsError.message }

  const { error: draftsError } = await db
    .from('response_drafts')
    .delete()
    .eq('survey_id', surveyId)
  if (draftsError) return { success: false, error: draftsError.message }

  const { error: tokensError } = await db
    .from('participation_tokens')
    .delete()
    .eq('survey_id', surveyId)
  if (tokensError) return { success: false, error: tokensError.message }

  const { error: responsesError } = await db
    .from('responses')
    .delete()
    .eq('survey_id', surveyId)
  if (responsesError) return { success: false, error: responsesError.message }

  const { error: metricsError } = await db
    .from('derived_metrics')
    .delete()
    .eq('survey_id', surveyId)
  if (metricsError) return { success: false, error: metricsError.message }

  const { error: themesError } = await db
    .from('qualitative_themes')
    .delete()
    .eq('survey_id', surveyId)
  if (themesError) return { success: false, error: themesError.message }

  const { error: deleteError } = await db
    .from('surveys')
    .delete()
    .eq('id', surveyId)

  if (deleteError) return { success: false, error: deleteError.message }
  return { success: true }
}
