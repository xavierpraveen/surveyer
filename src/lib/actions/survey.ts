const ALLOWED_TRANSITIONS: Record<SurveyStatus, SurveyStatus[]> = {
  draft: ['scheduled', 'open'],
  scheduled: ['open', 'draft'],
  open: ['closed'],
  closed: [],
}

// ─── Survey CRUD ──────────────────────────────────────────────────────────────

export async function createSurvey(
  input: unknown
): Promise<{ success: true; data: Survey } | { success: false; error: string }> {
  const parsed = createSurveySchema.safeParse(input)
  if (!parsed.success) {
    return { success: false, error: parsed.error.errors[0].message }
  }

  const supabase = await createSupabaseServerClient()
  const { data: { user }, error: authError } = await supabase.auth.getUser()
  if (authError || !user) {
    return { success: false, error: 'Unauthorized' }
  }

  const originalPayload: Record<string, unknown> = { ...parsed.data, created_by: user.id }
  let payload: Record<string, unknown> = { ...originalPayload }
  let lastError: { message?: string; code?: string } | null = null

  for (let attempt = 0; attempt < 6; attempt++) {
    const insertResult = await db
      .from('surveys')
      .insert(payload)
      .select()
      .single()

    if (!insertResult.error) {
      const survey = normalizeSurveyRow(insertResult.data as Record<string, unknown>)
      await ensureDefaultSurveySection(survey.id)
      return { success: true, data: survey }
    }

    lastError = insertResult.error as { message?: string; code?: string }
    if (!shouldRetryWithLegacySurveyWriteColumns(lastError)) {
      return { success: false, error: insertResult.error.message }
    }

    const missingColumn = getMissingColumnName(lastError)
    if (missingColumn === 'is_anonymous') {
      delete payload.is_anonymous
      payload.anonymous_mode = Boolean(parsed.data.is_anonymous)
      continue
    }
    if (missingColumn === 'anonymous_mode') {
      delete payload.anonymous_mode
      payload.is_anonymous = Boolean(parsed.data.is_anonymous)
      continue
    }
    if (missingColumn) {
      delete payload[missingColumn]
      continue
    }

    if ('public_link_enabled' in payload) {
      delete payload.public_link_enabled
      continue
    }

    payload = buildLegacySurveyPayload({ ...originalPayload })
  }

  if (!lastError) return { success: false, error: 'Failed to create survey' }
  return { success: false, error: lastError.message ?? 'Failed to create survey' }
}

export async function updateSurvey(
  input: unknown
): Promise<{ success: true; data: Survey } | { success: false; error: string }> {
  const parsed = updateSurveySchema.safeParse(input)
  if (!parsed.success) {
    return { success: false, error: parsed.error.errors[0].message }
  }

  const { id, ...fields } = parsed.data
  const modernUpdate = await db
    .from('surveys')
    .update(fields)
    .eq('id', id)
    .select()
    .single()

  if (!modernUpdate.error) {
    return { success: true, data: normalizeSurveyRow(modernUpdate.data as Record<string, unknown>) }
  }

  if (!shouldRetryWithLegacySurveyWriteColumns(modernUpdate.error)) {
    return { success: false, error: modernUpdate.error.message }
  }

  const legacyFields = buildLegacySurveyPayload(fields)
  const legacyUpdate = await db
    .from('surveys')
    .update(legacyFields)
    .eq('id', id)
    .select()
    .single()

  if (legacyUpdate.error) return { success: false, error: legacyUpdate.error.message }
  const survey = normalizeSurveyRow(legacyUpdate.data as Record<string, unknown>)
  await ensureDefaultSurveySection(survey.id)
  return { success: true, data: survey }
}

